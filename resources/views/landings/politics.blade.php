            <h1 class="text-center">Política de tratamiento de datos</h1>
            <p class="mt-5">
                La presente Política de Privacidad describe cómo se recopila, utiliza y protege la información personal 
                de los usuarios de la aplicación móvil <b>"Cumbre del Petróleo y Gas"</b> (en adelante, la "Aplicación"). 
                Esta Aplicación está diseñada para los asistentes al evento "<b>VII Cumbre de Petróleo, Gas y Energía</b>". <br> <br>
                1. <b>Información que recopilamos:</b> La Aplicación no permite el registro directo de usuarios. Los usuarios 
                se registran previamente en un portal gestionado por los organizadores del evento. Posteriormente, 
                los datos de registro son proporcionados a la Aplicación para habilitar el acceso. <br><br>

                <b>Recopilamos y almacenamos la siguiente información:</b> <br> 
                <li>Datos de acceso: Al utilizar el sistema de inicio de sesión, recogemos información como tu nombre de usuario y contraseña (cifrada), proporcionada por los organizadores del evento.</li>
                <li>Datos de uso: Información sobre cómo interactúas con la Aplicación (charlas a las que asignes como favoritos, comentarios, opiniones)</li> <br> <br>

                2. <b>Uso de la información Utilizamos la información recopilada para los siguientes fines:</b> <br> <br>

                    <li>Proveer acceso seguro a la Aplicación y sus funcionalidades.</li>
                    <li>Mejorar la experiencia del usuario, optimizando la interfaz y el rendimiento de la Aplicación.</li>
                    <li>Garantizar la seguridad de los datos.</li>
                    <li>Enviar notificaciones relacionadas con el evento o la Aplicación, si es necesario.</li> <br><br>
                
                3. <b>Compartición de datos con terceros La información recopilada no será vendida, alquilada ni compartida con terceros, excepto en los siguientes casos:</b> <br><br>

                    <li>Con los organizadores del evento, para la gestión del acceso de los asistentes.</li>
                    <li>Si es requerido por ley o en respuesta a una solicitud válida de las autoridades competentes.</li> <br><br>
                
                4. <b>Seguridad de la información:</b> Tomamos medidas técnicas y organizativas adecuadas para proteger tus datos
                contra el acceso no autorizado, la pérdida, el mal uso o la alteración. Las credenciales de inicio de 
                sesión están cifradas, y la transmisión de información se realiza a través de protocolos seguros (SSL). <br><br>

                5. <b>Retención de la información:</b> Los datos personales se almacenan únicamente durante el tiempo 
                necesario para cumplir con los fines descritos en esta Política, o mientras sea requerido por las 
                leyes aplicables. Una vez terminado el evento, la información de acceso será eliminada los treinta (90) primeros días posteriores a la finalización del evento. <br><br>

                6. <b>Derechos de los usuarios Como usuario, tienes los siguientes derechos sobre tus datos personales:</b> <br><br>

                <li>Acceder a tus datos personales.</li>
                <li>Solicitar la corrección de datos incorrectos o incompletos.</li>
                <li>Solicitar la eliminación de tus datos cuando ya no sean necesarios para los fines para los cuales fueron recogidos.</li>
                <li>Retirar tu consentimiento en cualquier momento para el procesamiento de tus datos.</li> <br>
                
                Para ejercer estos derechos, puedes ponerte en contacto con los organizadores del evento o a través del correo electrónico: <a href="mailto:app.cumbredelpetroleo@gmail.com ">app.cumbredelpetroleo@gmail.com</a>. <br><br>

                7. <b>Contacto:</b> Si tienes alguna pregunta o inquietud sobre esta Política de Privacidad, puedes contactarnos a través del correo electrónico: <a href="mailto:app.cumbredelpetroleo@gmail.com ">app.cumbredelpetroleo@gmail.com</a>.
            </p>
